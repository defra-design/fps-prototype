let editingProjectId = null;
let projectModal;
let activeProgramme = "";

function getSelectedProgramme() {
    const selectProgram = document.getElementById("select_program");
    const selectValue = selectProgram ? selectProgram.value.trim() : "";

    if (activeProgramme) {
        return activeProgramme;
    }

    if (selectValue && !selectValue.startsWith("--select")) {
        return selectValue;
    }

    return "";
}

function getFilteredProjects(programmeName) {
    const selectedProgramme = (programmeName || getSelectedProgramme() || "").trim().toLowerCase();

    if (!selectedProgramme || selectedProgramme.startsWith("--select")) {
        return [];
    }

    return projectData.filter(function (item) {
        return String(item.program || "").trim().toLowerCase() === selectedProgramme;
    });
}

document.addEventListener("DOMContentLoaded", function () {
    projectModal = new bootstrap.Modal(document.getElementById("projectModal"));
    renderProjectTable("");
});

function formatCurrency(value) {
    const number = parseFloat(String(value).replace(/[£,]/g, ""));
    if (isNaN(number)) {
        return "£0";
    }
    return "£" + number.toLocaleString();
}

function renderProjectTable(programmeName) {
    const tbody = document.getElementById("tableBody");
    const selectedProgramme = (programmeName || getSelectedProgramme() || "").trim();
    const filteredProjects = getFilteredProjects(selectedProgramme);

    if (!tbody) {
        return;
    }

    tbody.innerHTML = "";

    if (!selectedProgramme) {
        updateProjectTotals([]);

        const input = document.getElementById("selected-project-input");
        if (input) {
            input.value = "";
        }

        return;
    }

    if (filteredProjects.length === 0) {
        const emptyRow = document.createElement("tr");
        emptyRow.innerHTML = '<td colspan="14" class="text-center">No projects available for the selected programme.</td>';
        tbody.appendChild(emptyRow);
        updateProjectTotals(filteredProjects);

        const input = document.getElementById("selected-project-input");
        if (input) {
            input.value = "";
        }

        return;
    }

    filteredProjects.forEach(function (item, index) {
        const row = document.createElement("tr");

        row.style.cursor = "pointer";
        row.addEventListener("click", function (event) {
            if (!event.target.closest("button")) {
                selectProject(item.project, row);
            }
        });

        row.innerHTML = `
            <td>${item.project}</td>
            <td>${item.description}</td>
            <td>${item.manager}</td>
            <td>${item.program}</td>
            <td>${item.projectGroup}</td>
            <td>${item.customer}</td>
            <td>${item.contract}</td>
            <td>${item.disease}</td>
            <td>${item.status}</td>
            <td class="text-end">${item.budget}</td>
            <td class="text-end">${item.costInc}</td>
            <td class="text-end">${item.transInc}</td>
            <td class="text-end">${item.cwDebit}</td>
            <td class="text-AlignCenter">
                <button class="btn btn-sm btn-outline-primary" onclick="handleProjectEdit(${item.id})"><img src="../images/pen-to-square-regular-full.svg" alt="Edit icon for selected record" class="editstaffname" width="20"></button>
                <button class="btn btn-sm btn-outline-danger" onclick="handleProjectDelete(${item.id})"><img src="../images/trash-can-regular-full.svg" alt="Delete icon for selected record" width="20"></button>
            </td>
        `;

        tbody.appendChild(row);

        if (index === 0) {
            selectProject(item.project, row);
        }
    });

    updateProjectTotals(filteredProjects);
}

function selectProject(projectCode, selectedRow) {
    const input = document.getElementById("selected-project-input");
    if (input) {
        input.value = projectCode;
    }

    document.querySelectorAll("#tableBody tr").forEach(function (row) {
        row.classList.remove("govuk-table__row--selected");
        row.style.backgroundColor = "";
    });

    if (selectedRow) {
        selectedRow.style.backgroundColor = "#cce2f8";
    }
}

function handleProjectDelete(id) {
    if (confirm("Are you sure you want to delete this project?")) {
        projectData = projectData.filter(function (item) {
            return item.id !== id;
        });
        renderProjectTable(getSelectedProgramme());
    }
}

function handleProjectEdit(id) {
    const item = projectData.find(function (entry) {
        return entry.id === id;
    });

    if (!item) {
        return;
    }

    editingProjectId = id;

    document.getElementById("projectModalTitle").innerText = "Edit Project";
    document.getElementById("modal-project").value = item.project;
    document.getElementById("modal-description").value = item.description;
    document.getElementById("modal-manager").value = item.manager;
    document.getElementById("modal-program").value = item.program;
    document.getElementById("modal-customer").value = item.customer;
    document.getElementById("modal-projectGroup").value = item.projectGroup;
    document.getElementById("modal-contract").value = item.contract;
    document.getElementById("modal-disease").value = item.disease;
    document.getElementById("modal-status").value = item.status;
    document.getElementById("modal-budget").value = item.budget;
    document.getElementById("modal-costInc").value = item.costInc;
    document.getElementById("modal-transInc").value = item.transInc;
    document.getElementById("modal-cwDebit").value = item.cwDebit;

    const selectedProjectInput = document.getElementById("selected-project-input");
    if (selectedProjectInput) {
        selectedProjectInput.value = item.project;
    }

    projectModal.show();
}

function saveProject() {
    const currentProgramme = getSelectedProgramme();
    const project = document.getElementById("modal-project").value;
    const description = document.getElementById("modal-description").value;
    const manager = document.getElementById("modal-manager").value;
    const program = document.getElementById("modal-program").value || currentProgramme;
    const customer = document.getElementById("modal-customer").value;
    const projectGroup = document.getElementById("modal-projectGroup").value;
    const contract = document.getElementById("modal-contract").value;
    const disease = document.getElementById("modal-disease").value;
    const status = document.getElementById("modal-status").value;
    const budget = formatCurrency(document.getElementById("modal-budget").value);
    const costInc = formatCurrency(document.getElementById("modal-costInc").value);
    const transInc = formatCurrency(document.getElementById("modal-transInc").value);
    const cwDebit = formatCurrency(document.getElementById("modal-cwDebit").value);

    if (editingProjectId) {
        const index = projectData.findIndex(function (item) {
            return item.id === editingProjectId;
        });

        projectData[index] = {
            ...projectData[index],
            project,
            description,
            manager,
            program,
            customer,
            projectGroup,
            contract,
            disease,
            status,
            budget,
            costInc,
            transInc,
            cwDebit
        };

        editingProjectId = null;
    } else {
        const newId = projectData.length > 0 ? Math.max(...projectData.map(function (item) {
            return item.id;
        })) + 1 : 1;

        projectData.push({
            id: newId,
            project,
            description,
            manager,
            program,
            projectGroup,
            customer,
            contract,
            disease,
            status,
            budget,
            costInc,
            transInc,
            cwDebit
        });
    }

    renderProjectTable(program);
    projectModal.hide();
}

function updateProjectTotals(filteredProjects) {
    let totalBudget = 0;
    let totalCost = 0;
    let totalTrans = 0;
    let totalDebit = 0;

    filteredProjects.forEach(function (item) {
        totalBudget += parseFloat(String(item.budget).replace(/[£,]/g, "")) || 0;
        totalCost += parseFloat(String(item.costInc).replace(/[£,]/g, "")) || 0;
        totalTrans += parseFloat(String(item.transInc).replace(/[£,]/g, "")) || 0;
        totalDebit += parseFloat(String(item.cwDebit).replace(/[£,]/g, "")) || 0;
    });

    const inputs = document.querySelectorAll(".financial-summary input");
    if (inputs.length < 4) {
        return;
    }

    inputs[0].value = formatCurrency(totalBudget);
    inputs[1].value = formatCurrency(totalCost);
    inputs[2].value = formatCurrency(totalTrans);
    inputs[3].value = formatCurrency(totalDebit);
}

function updateTableBodyVisibility(programmeName) {
    activeProgramme = (programmeName || "").trim();
    renderProjectTable(activeProgramme);
}

window.updateTableBodyVisibility = updateTableBodyVisibility;
