    paginationUl.innerHTML = "";

    // Previous button
    const prevLi = document.createElement("li");
    prevLi.className = `govuk-pagination__item${currentPage1 === 1 ? " disabled" : ""}`;
    prevLi.setAttribute("aria-disabled", currentPage1 === 1);
    const prevA = document.createElement("a");
